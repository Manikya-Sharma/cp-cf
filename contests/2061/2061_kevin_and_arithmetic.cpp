void solution_fn() {
    int n;
    cin >> n;
    vi a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    bool has_even = false;
    int odd_count = 0;
    for (auto& elem : a) {
        if (elem % 2) {
            odd_count++;
        } else {
            has_even = true;
        }
    }
    if (!has_even) {
        odd_count -= 2;
    }
    odd_count = max(0, odd_count + 1);
    cout << odd_count << nl;
}

