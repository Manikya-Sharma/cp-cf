void solution_fn() {
    int n;
    cin >> n;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> b[i];
    }
    for (int i = 0; i < n; i++) {
        if (a[i] > b[i]) {
            cout << "NO" << nl;
            return;
        }
        if (a[i] == b[i])
            continue;
        if (b[(i + 1) % n] < b[i] - 1) {
            cout << "NO" << nl;
            return;
        }
    }
    cout << "YES" << nl;
}

