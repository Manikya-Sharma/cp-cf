void solution_fn() {
    int n;
    cin >> n;
    vi a(n);
    vi b(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> b[i];
    }
    b.push_back(0);
    int diff = 0;
    for (int i = 0; i < n; i++) {
        diff += max(0, a[i] - b[i + 1]);
    }
    cout << diff << nl;
}

