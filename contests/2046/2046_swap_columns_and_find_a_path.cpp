/* *** SOLUTION *** */

void solution_fn() {
    int n;
    cin >> n;
    vi a(n), b(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> b[i];
    }

    ll score = 0;
    for (int i = 0; i < n; i++) {
        score += max(a[i], b[i]);
    }

    int max_min = -1e9;
    for (int i = 0; i < n; i++) {
        max_min = max(min(a[i], b[i]), max_min);
    }
    cout << score + max_min << nl;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int test_cases;
    cin >> test_cases;
    while (test_cases--) {
        solution_fn();
    }
    return 0;
}
