void solution_fn() {
    int n;
    cin >> n;
    vi a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n - 1; i++) {
        if (a[i] > a[i + 1]) {
            cout << "NO" << nl;
            return;
        }
        a[i + 1] -= a[i];
        a[i] = 0;
    }
    cout << "YES" << nl;
}

