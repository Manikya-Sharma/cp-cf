/* *** SOLUTION *** */

void solution_fn() {
    int n;
    cin >> n;
    vi a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    int max_rep = 0;
    int rep = 0;
    for (int i = 1; i < n; i++) {
        if (a[i] == a[i - 1]) {
            rep++;
            max_rep = max(max_rep, rep);
        } else {
            rep = 0;
        }
    }
    cout << n - max_rep - 1 << nl;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int test_cases;
    cin >> test_cases;
    while (test_cases--) {
        solution_fn();
    }
    return 0;
}
